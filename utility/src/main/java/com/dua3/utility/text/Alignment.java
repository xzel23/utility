package com.dua3.utility.text;

/**
 * Alignment.
 */
public enum Alignment {
    /**
     * Left aligned.
     */
    LEFT,
    /**
     * Center aligned.
     */
    CENTER,
    /**
     * Right aligned.
     */
    RIGHT,
    /**
     * Align distributed.
     */
    DISTRIBUTE,
    /**
     * Align justified.
     *
     * <p>When using justified alignment, all text is aligned distributed for all but the last line, which is left
     * aligned.
     */
    JUSTIFY
}
